public class Cat {
    private long id;
    private String name;

    public Cat() {
    }

    public void setId(long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Cat(long id, String name) {
        this.id = id;
        this.name = name;
    }
}
